    private void btn5SearchSeatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn5SearchSeatsActionPerformed
        // TODO add your handling code here:
        String min_seats = TextField5MinSeats.getText();
        try{
             Integer.parseInt(min_seats);
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a number for Minimum Seats!");
            return;
        }
        if(Integer.parseInt(min_seats)>10 || Integer.parseInt(min_seats)<0){
            JOptionPane.showMessageDialog(null, "Minimum Seats should be between 0 and 10");
            return;
        }
        
        String max_seats = TextField5MaxSeats.getText();
        try{
             Integer.parseInt(max_seats);
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a number for Maximum Seats!");
            return;
        }
        if(Integer.parseInt(max_seats) < Integer.parseInt(min_seats)){
            JOptionPane.showMessageDialog(null, "Maximum Seats cannot be less than Minimum seats!");
            return;
        }
        if(Integer.parseInt(max_seats)>10 || Integer.parseInt(max_seats)<0){
            JOptionPane.showMessageDialog(null, "Maximum Seats should be between 0 and 10.");
            return;
        }
        
        int i=5;
        FilterResultsJPanel panel = new FilterResultsJPanel(userProcessContainer, carslist, TextField5MinSeats.getText(), TextField5MaxSeats.getText(), i);
        userProcessContainer.add("FilterResultsJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btn5SearchSeatsActionPerformed

