    private void btn6SearchSerialNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn6SearchSerialNoActionPerformed
        // TODO add your handling code here:
        String Avset;
        int i=0;
        
        try{
            Integer.parseInt(TextField6SerialNo.getText());
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please type a number for Sr. No.!");
            return;
        }
        
        for(Car car : carList){
            if(car.getSerial_num()==Integer.parseInt(TextField6SerialNo.getText())){
                if(car.isAvailable())
                    Avset = "Yes";
                else
                    Avset = "No";
                label6SerialNo.setText("Sr. No: " + car.getSerial_num() + "  |  Brand: " + car.getBrand() + "  |  Model: " + car.getModel_num() + "  |  City: " + car.getAvailble_city() + "  |  Available: " + Avset);
                break;
            }
            i++;
        }
        if(i>=carList.size())
            JOptionPane.showMessageDialog(null, "Car Not Found!");
    }//GEN-LAST:event_btn6SearchSerialNoActionPerformed

